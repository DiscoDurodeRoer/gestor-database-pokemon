    private void btnGuardarNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarNuevoActionPerformed

        String[] filaCombobox = (String[]) (this.cmbPokemon.getSelectedItem());
        int codigoPokemon = Integer.parseInt(filaCombobox[0]);

        filaCombobox = (String[]) (this.cmbMovimiento.getSelectedItem());
        int codigoMovimiento = Integer.parseInt(filaCombobox[0]);

        filaCombobox = (String[]) (this.cmbTipoAprendizaje.getSelectedItem());
        int codigoTipoApredizaje = Integer.parseInt(filaCombobox[0]);

        filaCombobox = (String[]) (this.cmbFormaAprendizaje.getSelectedItem());
        int codigoFormaApredizaje = Integer.parseInt(filaCombobox[0]);

        String errores = "";

        if (codigoPokemon == -1) {
            errores += " - Debes seleccionar un pokemon\n";
        }

        if (codigoMovimiento == -1) {
            errores += " - Debes seleccionar un movimiento\n";
        }

        if (codigoTipoApredizaje == -1) {
            errores += " - Debes seleccionar un tipo de aprendizaje\n";
        }

        if (codigoFormaApredizaje == -1) {
            errores += " - Debes seleccionar un pokemon\n";
        }

        if (errores.equals("")) {

            try {
                String sql = "insert into pokemon_movimiento_forma values(" + codigoPokemon + ", " + codigoMovimiento + ", " + codigoFormaApredizaje + ");";

                VariablesGlobales.conexion.ejecutarInstruccion(sql);

                Texto.escribirFicheroCaracteresBuf("enlazar_movimientos.txt", sql + "\r\n", true);

                JOptionPane.showMessageDialog(this, "Insertado correctamente", "Exito", JOptionPane.ERROR_MESSAGE);
            } catch (SQLException ex) {
                System.out.println(ex.getMessage());
            }

        } else {
            JOptionPane.showMessageDialog(this, errores, "Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnGuardarNuevoActionPerformed

