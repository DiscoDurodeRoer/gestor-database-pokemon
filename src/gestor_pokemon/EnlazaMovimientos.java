    private void rellenaFormaAprendizaje() {
        String[] filaCombobox = (String[]) (this.cmbTipoAprendizaje.getSelectedItem());
        int codigoTipoAprendizaje = Integer.parseInt(filaCombobox[0]);

        if (codigoTipoAprendizaje != -1) {

            String table = "";
            String columna = "";

            switch (codigoTipoAprendizaje) {
                case 1:
                    table = "mt";
                    columna = "mt";

                    btnAniadirTipo.setText("A単adir MT...");
                    break;
                case 2:
                    table = "mo";
                    columna = "mt";
                    btnAniadirTipo.setText("A単adir MO...");
                    break;
                case 3:
                    table = "nivel_aprendizaje";
                    columna = "nivel";
                    btnAniadirTipo.setText("A単adir nivel...");
                    break;
            }

            datosFormaAprendizaje[0] = table;
            datosFormaAprendizaje[1] = columna;
            datosFormaAprendizaje[2] = codigoTipoAprendizaje+"";
            

            String sql = "select id_forma_aprendizaje, " + columna + " "
                    + "from " + table + " order by " + columna;

            VariablesGlobales.conexion.rellenaComboBox2Columnas(this.cmbFormaAprendizaje,
                    sql,
                    "--Seleccione una forma aprendizaje--",
                    "id_forma_aprendizaje",
                    columna);

        }
    }

